package com.toannk.project.dataset;

public class EquipmentInDestiny {
	private int idEquipment;
	private int idDestiny;
	private int equipmentQuantity;
	private String name;
	
	

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the idEquipment
	 */
	public int getIdEquipment() {
		return idEquipment;
	}
	/**
	 * @param idEquipment the idEquipment to set
	 */
	public void setIdEquipment(int idEquipment) {
		this.idEquipment = idEquipment;
	}
	/**
	 * @return the idDestiny
	 */
	public int getIdDestiny() {
		return idDestiny;
	}
	/**
	 * @param idDestiny the idDestiny to set
	 */
	public void setIdDestiny(int idDestiny) {
		this.idDestiny = idDestiny;
	}
	/**
	 * @return the equipmentQuantity
	 */
	public int getEquipmentQuantity() {
		return equipmentQuantity;
	}
	/**
	 * @param equipmentQuantity the equipmentQuantity to set
	 */
	public void setEquipmentQuantity(int equipmentQuantity) {
		this.equipmentQuantity = equipmentQuantity;
	}
	
	
}
